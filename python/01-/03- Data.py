# --------------------------------
# type() 
# All Data in python is Object 
# --------------------------------

print(10)
print(type(10)) # int 

print(type(100.9)) # float 

print(type("Hello")) #str 

print(type([1,2,3,4,5])) # list

print(type((1,2,3,4,5))) # tuple

print(type({"one":1,"Two":2})) #dict 

print(type(2==2)) #boolean 

